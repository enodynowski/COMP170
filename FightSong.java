public class FightSong {
	public static void main(String[] args) {
		PrintGoTeamGo();
		System.out.println();
		PrintChorus();
		System.out.println();
		PrintChorus();
		System.out.println();
		PrintGoTeamGo();
	}
	public static void PrintChorus() {
		PrintGoTeamGo();
		PrintBestInWest();
		PrintGoTeamGo();
	}
	public static void PrintGoTeamGo() {
		System.out.println("Go, team, Go! \nYou can do it.");
	}
	public static void PrintBestInWest() {
		System.out.println("You're the best,\nin the West.");
	}
}
